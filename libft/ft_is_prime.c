int		ft_is_prime(int n)
{
	int		i;

	i = 2;
	if (n < 2)
		return (0);
	if (n == 2 || n == 3)
		return (1);
	while (i <= n / 2)
	{
		if (n % i++ == 0)
			return (0);
	}
	return (1);
}
